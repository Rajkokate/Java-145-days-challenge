//Assingment Operator

public class assing {
    public static void main(String[] args) {
        int a = 10;
        //a = a + 20
        a += 20; // Here we use += operator

        int b = 10;
        //b = b - 5;
        b -= 5; // Here we use -= operator

        int c = 5;
        //c = c * 5;
        c *= 5; // we use *=

        int d = 10;
        //d = d / 5;
        d /= 5; // we use /=

        int e = 10;
        //e = e / 5;
        e %= 5; // we use %=



        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        
        

    }
    
}
